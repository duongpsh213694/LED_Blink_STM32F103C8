GPIOB_ODR  EQU 0x40010C0C				

	AREA mySection3, CODE, READONLY
       
myApplication

    ; Set up clock for Port B
	LDR R0,=GPIO_RCC_R
	MOV R1,#0x08
	STR R1,[R0]
	
	; Set up Output in PB12, Input button in PB13 and PB15
	LDR R0,=GPIOB_CRH
	MOV32 R1,#0x80820000
	STR R1,[R0]
	
	; Constant values
	MOV32 R4,#440000 ; 20% of 2s
	MOV32 R5,#220000 ; 10% of 2s
	MOV32 R6,#2000000 ; 90% of 2s
	MOV R7,#0 ; Count on cycle state
	MOV R8,#0; Count on Array
	LDR R9,=SinTable ; Sin Table for breath
	
loop  
; main engine goes here
	 LDR R0,=GPIOB_IDR
	 LDR R1,[R0] 
	 ; R2 stores the bit value of Change button in PB13
	 AND R2,R1,#0x00002000
	 LSR R2,R2,#13
	 ; R3 stores the bit value of Breath button in PB15
	 LDR R1,[R0]
	 AND R3,R1,#0x00008000
	 LSR R3,R3,#15
	 ; Checking if Breath button is pressed
	 CMP R3,#1
	 BEQ Breath 
	 ; Checking if Change button is pressed
	 CMP R2,#1
	 BEQ Change 
	 
	 ; LED 
	 LDR R12,=GPIOB_ODR
	 MOV32 R1,#0x00001000
	 STR R1,[R12]
	 MUL R10,R7,R4
	 ADD R10,R10,R5
	 BL DelayOn
	 MOV32 R1,#0x00000000
	 STR R1,[R12]
	 MUL R10,R7,R4
	 SUB R10,R6,R10
	 BL DelayOff
	 
	 B    loop
		
   ALIGN 4 
Breath
	LDRH R10,[R9] ; Assigned value from SinTable
	LDR R12,=GPIOB_ODR
	MOV32 R1,#0x00001000
	STR R1,[R12]
	BL DelayOn ; On with the value from R10
	
	LDR R12,=GPIOB_ODR
	MOV32 R1,#0x00000000
	STR R1,[R12]
	MOV32 R11,#10000
	SUB R10,R11,R10
	BL DelayOff ; Off with the value from R10
	
	ADD R9,R9,#2
	ADD R8,R8,#1
	CMP R8,#100
	BEQ ResetCount
	
	LDR R0,=GPIOB_IDR
	LDR R1,[R0]
	AND R3,R1,#0x00008000 ; Save breath button to R3
	LSR R3,R3,#15
	CMP R3,#1 ; check if Breath button is still being pressed
	BEQ Breath ; Loop until the Breath button is released
	MOV R7,#1
	B loop;
	
Change
	LDR R0,=GPIOB_IDR
	LDR R1,[R0]
	AND R2,R1,#0x00002000 ; Save change button to R2
	LSR R2,R2,#13
	CMP R2,#1 ; Check if Change button is released
	BEQ Change ; Loop until Change button is released
	ADD R7,R7,#1
	CMP R7,#5 ; checking if cycle exceeds 90%
	BEQ ResetCycle
	B loop

ResetCount
	MOV R8,#0
	LDR R9,=SinTable
	B loop

ResetCycle
	MOV R7,#0
	B loop

DelayOn ; Deley On 
	NOP ;dummy operation
	NOP
	NOP
	NOP
	SUBS R10,R10,#1
	BNE DelayOn
	BX LR	
DelayOff ; Deley Off 
	NOP ;dummy operation
	NOP
	NOP
	NOP
	SUBS R10,R10,#1
	BNE DelayOff
	BX LR	
